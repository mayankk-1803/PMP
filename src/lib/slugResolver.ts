/**
 * Centralized function to format and clean product titles.
 * Removes autogenerated prefixes/suffixes, random numbers, and internal identifiers.
 */
export const cleanProductTitle = (title: string | null | undefined): string => {
  if (!title) return "";
  const trimmed = title.trim();
  if (trimmed.startsWith("PacMyProduct")) {
    return "PacMyProduct";
  }
  
  return trimmed
    .replace(/\s*\(\d+\)$/gi, "")
    .replace(/\s*copy$/gi, "")
    .replace(/\s+sub(?:\s+\d+|\s+-\s+\d+|\b)/gi, "")
    .replace(/\s+sub\s*-\s*\d+$/gi, "")
    .replace(/\s+sub\s+\d+$/gi, "")
    .replace(/[-_]\s*\d+$/g, "")
    .replace(/\s+\d+$/g, "")
    .trim();
};

